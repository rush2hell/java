class Calculator
{
	public static void main(String args[])
	{
		System.out.println("Welcome to Java Calculator");
		//addition
		int add1=10;
		int add2=10;
		int add_number=add1+add2;
		System.out.println("Addition of Numbers is :"+add_number);
		//subtraction
		int sub1=20;
		int sub2=10;
		int sub_number=sub1+sub2;
		System.out.println("Subtraction of numbers is :"+sub_number);
		//division
		int div1=20;
		int div2=10;
		int div_number=div1+div2;
		System.out.println("Division of number is :"+div_number);
		//multiplication
		int mul1=10;
		int mul2=10;
		int mul_number=mul1*mul2;
		System.out.println("Multiplication of numbers is :"+mul_number);
	}
}